const bannerContent = document.querySelectorAll('.banner-content');
const bannerNav = document.querySelectorAll('.banner-nav button');

let currentIndex = 0;
let intervalId;

function showImage(index) {
  bannerContent[currentIndex].classList.remove('active');
  bannerNav[currentIndex].classList.remove('active');

  bannerContent[index].classList.add('active');
  bannerNav[index].classList.add('active');

  currentIndex = index;
}

function startInterval() {
  intervalId = setInterval(() => {
    let nextIndex = currentIndex + 1;

    if (nextIndex >= bannerContent.length) {
      nextIndex = 0;
    }

    showImage(nextIndex);
  }, 3000);
}

startInterval();

bannerNav.forEach((button, index) => {
  button.addEventListener('click', () => {
    clearInterval(intervalId);
    showImage(index);
    startInterval();
  });
});

let currentSlide = 0;
const slides = document.querySelectorAll(".slide");
const slideCount = slides.length;

function nextSlide() {
  slides[currentSlide].classList.remove("active");
  currentSlide = (currentSlide + 1) % slideCount;
  slides[currentSlide].classList.add("active");
}

setInterval(nextSlide, 3000);
